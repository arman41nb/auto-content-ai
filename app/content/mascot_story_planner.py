def _generic_plan(topic: str, niche: str, mascot: MascotProfile) -> MascotStoryPlan:
    return MascotStoryPlan(
        topic=topic,
        niche=niche or "science",
        title=topic.rstrip("?"),
        story_angle="Miko turns the question into a concrete miniature story.",
        audience="Curious viewers who want a useful short explainer.",
        core_question=topic,
        simple_answer="Start with a concrete example, follow the mechanism, then keep the caveat attached.",
        analogy="A complicated system is easier to understand when one small object carries the pressure through the scene.",
        caveat="This is a simplified educational explanation.",
        caption="Miko explains the question with a story, a simple analogy, a mechanism, an example, and a caveat.",
        hashtags=["explained", "learn", "education"],
        mascot_id=mascot.mascot_id,
        scenes=[
            MascotStoryScene(
                scene_number=index,
                role=role,
                duration_target=3.0,
                visual_type=visual_type,
                visual_goal=f"Miko explains {topic} through a concrete visual scene.",
                mascot_action="explaining with a tiny prop" if visual_type in {"mascot_ai", "mixed"} else "none",
                setting="clean miniature educational scene",
                media_query=topic,
                voiceover_line=line,
                on_screen_caption=caption,
                fact_claim=line,
                source_needed=False,
                emotion="curious",
            )
            for index, (role, visual_type, line, caption) in enumerate(
                [
                    ("hook", "mascot_ai", f"Here is the simple version of {topic.rstrip('?')}.", "START HERE"),
                    ("question", "mixed", "First, turn the big question into one small scene.", "THE QUESTION"),
                    ("analogy", "object_scene_ai", "A simple analogy makes the pressure visible.", "THE ANALOGY"),
                    ("mechanism", "chart_motion", "Then follow the chain from cause to response.", "THE LINK"),
                    ("example", "broll_photo", "A real-world example makes it easier to remember.", "REAL WORLD"),
                    ("twist", "mixed", "The caveat keeps the answer honest.", "THE TRAP"),
                    ("takeaway", "mascot_ai", "The best answer is simple, but not oversimplified.", "THE TAKEAWAY"),
                ],
                start=1,
            )
        ],
    )
