def plan_mascot_story_reel(topic: str, niche: str, mascot: MascotProfile) -> MascotStoryPlan:
    normalized = " ".join(topic.strip().split()) or "What is the relationship between oil prices and the dollar?"
    if "oil" in normalized.lower() and "dollar" in normalized.lower():
        return _oil_dollar_plan(normalized, niche, mascot)
    return _generic_plan(normalized, niche, mascot)


